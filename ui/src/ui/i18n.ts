export let currentLang: Language = (typeof localStorage !== "undefined" ? localStorage.getItem("zero-lang") as Language : null) || "pt-BR";

export function getLanguage(): Language {
    return currentLang;
}

export function setLanguage(lang: Language) {
    currentLang = lang;
    if (typeof localStorage !== "undefined") {
        localStorage.setItem("zero-lang", lang);
    }
    if (typeof window !== "undefined") {
        window.location.reload();
    }
}

export function t(key: keyof typeof translations["pt-BR"], params?: Record<string, string | number>): string {
    let text = translations[currentLang][key] || translations["pt-BR"][key] || key;
    if (params) {
        for (const [k, v] of Object.entries(params)) {
            text = text.replace(`{${k}}`, String(v));
        }
    }
    return text;
}
